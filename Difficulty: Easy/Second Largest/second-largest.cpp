// User function template for C++
class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int maxi = 0, second_maxi = -1;
        for(auto x : arr) maxi = max(x, maxi);
        for(auto y : arr) if(y < maxi && y > second_maxi) second_maxi = y;
        return second_maxi;
    }
};

